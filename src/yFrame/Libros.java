    private void jButton13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton13MouseClicked
        // Obtener el nombre del libro desde jTextField4
    String nombreLibro = jTextField4.getText().trim();

    // Verificar si el campo está vacío
    if (nombreLibro.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Error: El nombre del libro no puede estar en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Buscar el libro en la lista de libros
    Boolean libroEncontrado = false;
    for (Libro libro : Prestamos.getlibros()) {
        if (libro.getTitulo().equalsIgnoreCase(nombreLibro)) {
            libroEncontrado = true;
            break;
        }
        else{
            libroEncontrado = false;
            break;
        }
    }

    
    if (libroEncontrado) {
        jTextArea1.setText("El libro "  +  nombreLibro  + " se encuentra disponible");
        
    }

    else  {
        jTextArea1.setText("No se encuetra disponible");
    } 
    }//GEN-LAST:event_jButton13MouseClicked

