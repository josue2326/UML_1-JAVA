    private void jButton14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton14MouseClicked
    String nombre = JOptionPane.showInputDialog(null, "Digite el nombre del libro");
    String ISBN = JOptionPane.showInputDialog(null, "Digite el ISBN del libro");

    // Verificar si alguno de los campos está en blanco
    if (nombre == null || nombre.trim().isEmpty() || ISBN == null || ISBN.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Error: El nombre y el ISBN no pueden estar en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Verificar si el libro ya existe en la colección
    for (Libro libro : Prestamos.getlibros()) {
        if (libro.getIsbn().equals(ISBN) || libro.getTitulo().equalsIgnoreCase(nombre)) {
            JOptionPane.showMessageDialog(null, "Error: El libro ya existe en la colección", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    // Si pasa las verificaciones, agregar el libro a la colección
    Libro newlibro = new Libro(ISBN, nombre);
    Prestamos.getlibros().add(newlibro);
    JOptionPane.showMessageDialog(null, "El libro de nombre: " + nombre + " fue registrado");
    }//GEN-LAST:event_jButton14MouseClicked

